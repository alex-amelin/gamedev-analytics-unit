"""Страж целостности приёма: жёсткая сверка числа строк источник↔записанная партиция.

Считает, сколько строк-данных прислал источник (по сырому TSV Logs API — заголовок
вычитается в КАЖДОЙ части), и сравнивает это с числом строк, реально записанных в партицию
дня (возврат :func:`scripts.utils.parquet_store.write_partition`). Любое расхождение —
**жёсткий fail** (:class:`RowCountMismatchError`, наружу = non-zero), а НЕ ``warning``: это
осознанное отличие от directaiq (project-context: анти-паттерн «сверка строк как warning»).
Смысл — поймать молчаливую потерю/задвоение строк между источником и БД до того, как
неполный день просочится в базу (FR-13, опора NFR-1).

**Счёт источника — от первоисточника, независимо от парсинга.** ``expected`` считается
прямым подсчётом строк сырого TSV (минус заголовок на часть), а НЕ числом распарсенных p81
строк — иначе баг парсинга (выброшенная строка) замаскировался бы, ведь обе величины упали
бы одинаково. ``actual`` берётся из возврата ``write_partition`` (что реально записано).
Расхождение ловит потерю на участке источник→запись.

**Единый сплиттер границ строк (defer 2.3).** Логика «как сырой TSV режется на строки»
живёт в ОДНОМ месте — :func:`split_tsv_rows`. И счёт сверки (:func:`count_part_rows`), и
парсинг дня в оркестраторе p81 (2.7) зовут её, поэтому ``expected`` и число распарсенных
строк согласованы по границам строк: misfire (ложный жёсткий fail на валидном дне или
взаимная маскировка реальной потери) исключён по построению.

**Заголовок в КАЖДОЙ части (off-by-P-гард).** Logs API отдаёт каждую часть отдельной
выгрузкой со своим TSV-заголовком, поэтому счёт идёт НА УРОВНЕ части
(``непустых_строк − 1``) и суммируется по частям. Наивное «всего строк во всех частях − 1»
при ``P > 1`` частях дало бы off-by-``(P−1)``.

**Чистый stdlib-примитив (без сети/файлов/БД).** Модуль НЕ импортирует ``parquet_store``,
НЕ читает партицию, НЕ открывает ``gdau.duckdb``, НЕ пишет ``load_state``, НЕ берёт
``.writer.lock``. На вход — числа и сырой TSV (байты/строки), на выход — решение
«сошлось / жёсткий fail». Поэтому тестируется без сети, без DuckDB и без партиций.

**Границы (что делает ДРУГОЙ компонент):** атомарную запись партиции — ``parquet_store``
(2.2); отметку «день загружен» в ``load_state`` — 2.4 (2.3 лишь ГЕЙТит её исключением, сам
мету не пишет); ``.writer.lock`` — 2.5; типизированные view с ``TRY_CAST`` — 2.6; сборку дня
из TSV-частей и весь цикл приёма — оркестратор p81 (2.7).
"""

from __future__ import annotations

import logging
from collections.abc import Iterable

logger = logging.getLogger(__name__)

__all__ = [
    "RowCountMismatchError",
    "split_tsv_rows",
    "count_part_rows",
    "count_source_rows",
    "verify_row_count",
]


class RowCountMismatchError(RuntimeError):
    """Сверка строк источник↔партиция не сошлась — runtime-инцидент целостности приёма.

    Подкласс :class:`RuntimeError` (несошедшаяся сверка — не баг аргумента, а нарушение
    инварианта во время выполнения, поэтому НЕ :class:`ValueError`). Выделенный тип даёт
    оркестратору p81 (2.7) точный ``except`` для «день НЕ помечать загруженным» (AC #2) и
    отделяет его от ОС-/значимостных ошибок. Контекст (источник/дата/expected/actual) — в
    тексте сообщения; дополнительных полей нет (простота).
    """


def split_tsv_rows(part: bytes | str) -> list[str]:
    r"""Разбить одну TSV-часть Logs API на непустые строки (вкл. заголовок) — общий сплиттер.

    **Единый шов границ строк для всего приёма (defer 2.3).** И сверка
    (:func:`count_part_rows`), и парсинг дня в оркестраторе p81 (2.7) обязаны резать сырой
    TSV **одинаково** — иначе ``expected`` (счёт сверки) разойдётся с числом распарсенных
    строк на валидном дне (ложный жёсткий fail) либо обе ошибки замаскируют реальную потерю.
    Поэтому логика границ строк живёт здесь **одна**, а оба потребителя зовут её.

    ``bytes`` декодируются как UTF-8. Строки режутся РОВНО по ``\n`` (НЕ ``str.splitlines`` —
    тот режет ещё и ``\v``/``\f``/``\x1c``-``\x1e``/``\x85``/LINE SEPARATOR/PARAGRAPH
    SEPARATOR, инфлейтя счёт); хвостовой ``\r`` из ``\r\n`` срезается, пустые строки
    отбрасываются (хвостовой перевод строки не добавляет лишнюю запись). Заголовок части
    **остаётся** в результате (его вычитает :func:`count_part_rows`, а p81 берёт как ``[0]``
    для схемы) — сам сплиттер про заголовок ничего не знает.

    :param part: сырая TSV-часть (``bytes`` от Logs API или уже декодированная ``str``).
    :returns: список непустых строк части в исходном порядке (включая заголовок, если он есть).
    """
    text = part.decode("utf-8") if isinstance(part, bytes) else part
    # Режем РОВНО по "\n" (НЕ str.splitlines): splitlines режет ещё и управляющие/юникод-
    # разделители (\v, \f, \x1c-\x1e, \x85, LINE/PARAGRAPH SEPARATOR) и одиночный \r — это
    # инфлейтило бы счёт на валидном дне. Хвостовой \r из \r\n срезаем ДО проверки пустоты,
    # чтобы пустые CRLF-строки не считались за данные.
    return [s for line in text.split("\n") if (s := line.rstrip("\r")) != ""]


def count_part_rows(part: bytes | str) -> int:
    r"""Число строк-**данных** в одной TSV-части Logs API (заголовок исключён; AC #3).

    Одна часть = одна выгрузка ``download_log_request_part`` со своей строкой-заголовком.
    Границы строк режет общий :func:`split_tsv_rows` (тот же сплиттер, что использует
    парсинг дня в p81 2.7 — единый шов, defer 2.3), затем вычитается **один** заголовок части.

    :param part: сырая TSV-часть (``bytes`` от Logs API или уже декодированная ``str``).
    :returns: ``max(0, непустых_строк − 1)``. Пустая часть и часть только с заголовком → ``0``.
    """
    # Минус ОДИН заголовок ЭТОЙ части (off-by-P-гард — вычитание на часть, не на источник).
    return max(0, len(split_tsv_rows(part)) - 1)


def count_source_rows(parts: Iterable[bytes | str]) -> int:
    """Суммарное число строк-данных источника по всем TSV-частям (AC #3, #4).

    Сумма :func:`count_part_rows` по частям — заголовок вычитается в каждой части
    (off-by-P-гард, риск №3). Дедуп НЕ делается: сырьё хранится дословно (FR-6), дубли строк
    считаются, а не схлопываются (AC #4).

    :param parts: итерируемое TSV-частей. Передавать именно **итерируемое частей**, а не один
        ``bytes`` (он сам итерируем по байтам — footgun); одна часть → ``[payload]``.
    :returns: сумма строк-данных по частям. Ноль частей → ``0`` (легитимно пустой день).
    :raises TypeError: ``parts`` — одиночная ``bytes``/``str`` вместо итерируемого частей.
        Без гарда ``str`` перебралась бы по символам и дала бы тихий ``0`` (потеря данных), а
        ``mypy`` этого НЕ ловит (``str`` ⊂ ``Iterable[bytes | str]``). Делаем footgun громким.
    """
    if isinstance(parts, (bytes, str)):
        raise TypeError(
            "count_source_rows ожидает ИТЕРИРУЕМОЕ TSV-частей, а не одну часть: одиночная "
            f"{type(parts).__name__} перебралась бы по символам/байтам и дала бы тихий "
            "неверный счёт. Оберни одну часть в список: [payload]."
        )
    return sum(count_part_rows(part) for part in parts)


def verify_row_count(expected: int, actual: int, *, source: str, date: str) -> None:
    """Жёстко сверить число строк источник↔партиция; расхождение → исключение (AC #1, #2, #4).

    Равенство (включая ``0 == 0`` для легитимно пустого дня) → лог INFO + ``None``.
    Расхождение → :class:`RowCountMismatchError` (наружу = non-zero), **никогда** ``warning``
    (осознанное отличие от directaiq, риск №4). Сам ``load_state`` НЕ пишется: бросок ДО шага
    записи меты и есть гейт «день не помечать загруженным» (AC #2; запись меты — 2.4).

    :param expected: число строк-данных источника (см. :func:`count_source_rows`).
    :param actual: число записанных строк (возврат ``write_partition`` 2.2).
    :param source: ``visits``/``hits`` — только для диагностики сообщения.
    :param date: дата дня ``YYYY-MM-DD`` — только для диагностики сообщения.
    :raises ValueError: ``expected`` или ``actual`` отрицательны (защита от мусора вызывающего).
    :raises RowCountMismatchError: ``expected != actual`` (жёсткий fail целостности).
    """
    if expected < 0 or actual < 0:
        raise ValueError(
            f"Число строк не может быть отрицательным: expected={expected}, "
            f"actual={actual} (источник {source!r}, дата {date!r})."
        )
    if expected != actual:
        raise RowCountMismatchError(
            f"Сверка строк не сошлась: источник {source!r}, дата {date!r}, "
            f"источник дал {expected} строк, записано {actual}. "
            f"День НЕ помечается загруженным (расхождение источник↔партиция = жёсткий fail)."
        )
    logger.info(
        "Сверка строк сошлась: источник %s, дата %s, строк %d.",
        source,
        date,
        actual,
    )
